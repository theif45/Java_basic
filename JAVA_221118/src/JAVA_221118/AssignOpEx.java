package JAVA_221118;

public class AssignOpEx {

	public static void main(String[] args) {
		int result =10;
		
		System.out.println("변수 result : " + result);
		
		result = result + 1;
		System.out.println("result + 1 = " + result);
		
		result += 1;
		System.out.println("result + 1 = " + result);
	}
}
